public class Fornecedor {
    String nome;
    String email;
    String telefone;
    String data_de_nascimento;

    Fornecedor(){

    }
    Fornecedor(String nome, String email, String telefone, String data_de_nascimento)
    {
        this.nome = nome;
        this.email = email;
        this.telefone = telefone;
        this.data_de_nascimento = data_de_nascimento;

    }
}
